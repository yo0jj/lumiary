"""
위기 감지 모듈.
STT 결과에서 자해/자살 관련 표현을 감지하면 True를 반환한다.
"""

import re

_CRISIS_PATTERNS = re.compile(
    r"(자살|자해|죽고\s*싶|사라지고\s*싶|목숨을\s*끊|스스로\s*목숨|극단적\s*선택"
    r"|더\s*이상\s*살기\s*싫|살기\s*싫다|살고\s*싶지\s*않|죽어버리고\s*싶"
    r"|세상을\s*떠나고\s*싶|삶을\s*끝내|끝내고\s*싶다|자살\s*충동|자해\s*충동)",
    re.IGNORECASE,
)

CRISIS_RESPONSE = (
    "지금 정말 많이 힘드시겠어요. 그 마음이 느껴져서 저도 걱정이 돼요. "
    "혼자 감당하지 않아도 괜찮아요. "
    "지금 바로 자살예방상담전화 1393이나 정신건강위기상담전화 1577-0199에 연락해보실 수 있어요. "
    "24시간 연결돼 있고, 당신 이야기를 들어줄 사람이 있어요. 오늘 대화는 여기서 마무리할게요."
)


def detect_crisis(text: str) -> bool:
    """위기 표현이 포함된 경우 True 반환."""
    return bool(_CRISIS_PATTERNS.search(text))
